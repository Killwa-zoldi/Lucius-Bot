module.exports = {
  name: "مدح",
  description: "إرسال مدح عشوائي للشخص (بالرد أو المنشن)",

  run: async (sock, m, args) => {
    try {
      const from = m.key.remoteJid;

      // 📌 جلب الهدف (منشن أو رد)
      const context = m.message?.extendedTextMessage?.contextInfo;
      const target =
        context?.mentionedJid?.[0] ||
        context?.participant;

      if (!target) {
        return sock.sendMessage(
          from,
          { text: "❌ منشن الشخص أو رد على رسالته." },
          { quoted: m }
        );
      }

      // ❤️ تفاعل
      await sock.sendMessage(from, {
        react: { text: "🫦", key: m.key }
      });

      // 🌸 عبارات المدح
      const phrases = [
        "هل أنت تفاح؟ لأن لما أشوفك ارتاح 🫦",
        "هل تعلم أن ابتسامتك تسرق مني النوم 😏",
        "لو كنت نجمة، كنت أنورت السماء كلها 🌟",
        "أراك كل يوم، وقلبي ما يمل منك 💖",
        "هل تعرف أنك تجعل قلبي يرقص كلما تحدثت؟ 💃",
        "وجودك يخليني سعيد بدون سبب 😍",
        "هل أنت سحر؟ لأنك تخطف الأنظار دائمًا ✨",
        "كل مرة أراك فيها أشعر أن العالم جميل 🌹",
        "أريد أن أرسل لك قلب 💌 لأنك مميز",
        "هل أنت قهوة؟ لأنك تجعل يومي أفضل ☕",
        "هل تعرف أن صوتك يهدئ روحي؟ 🎶",
        "أنت السبب في ابتسامتي اليوم 😊",
        "هل أنت موسيقى؟ لأنك تملأ حياتي لحنًا 🎵",
        "أراك كالوردة بين الأشواك 🌸",
        "هل أنت شمس؟ لأنك تنير أيامي 🌞",
        "كلما رأيتك، أرى العالم أجمل 🌈",
        "هل أنت نسيم؟ لأنك تجعل قلبي يطير 🌬️",
        "وجودك يجعل كل شيء أفضل 💫",
        "أنت كالنجمة التي تضيء السماء ليليًا ✨",
        "هل تعلم أن عينيك تشبهان البحر؟ 🌊",
        "أحب طريقة ضحكتك 😄",
        "أنت كالكنز الذي لا يفقد قيمته 💎",
        "كل كلمة منك تجعل قلبي يفرح 💖",
        "أنت تجعل عالمي مليئًا بالضحك 😄",
        "أنت تجعل كل لحظة لا تُنسى 💫"
      ];

      // 🎲 اختيار عشوائي
      const phrase = phrases[Math.floor(Math.random() * phrases.length)];

      // 📤 إرسال
      await sock.sendMessage(from, {
        text: `@${target.split("@")[0]} ${phrase}`,
        mentions: [target]
      });

    } catch (err) {
      console.error("❌ خطأ أمر .مدح:", err);
      await sock.sendMessage(
        m.key.remoteJid,
        { text: "❌ حدث خطأ أثناء تنفيذ الأمر." },
        { quoted: m }
      );
    }
  }
};